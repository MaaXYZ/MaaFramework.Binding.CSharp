namespace MaaFramework.Binding;

/// <summary>
///     MaaFramework logging level.
/// </summary>
public enum LoggingLevel
{
    /// <summary>
    ///     Turns off logging.
    /// </summary>
    Off = 0,

    /// <summary>
    ///     Fatal level.
    /// </summary>
    Fatal = 1,

    /// <summary>
    ///     Error level.
    /// </summary>
    Error = 2,

    /// <summary>
    ///     Warning level.
    /// </summary>
    Warn = 3,

    /// <summary>
    ///     Warning level.
    /// </summary>
    Info = 4,

    /// <summary>
    ///     Debug level.
    /// </summary>
    Debug = 5,

    /// <summary>
    ///     Trace level.
    /// </summary>
    Trace = 6,

    /// <summary>
    ///     Logs all level.
    /// </summary>
    All = 7,
}
